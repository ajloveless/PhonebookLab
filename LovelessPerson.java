public class LovelessPerson {
    public String name, phoneNumber;
    public int age, idNumber, zipCode;

    public LovelessPerson(String name, int age, int idNumber, String phoneNumber, int zipCode) {
        this.name = name;
        this.age = age;
        this.idNumber = idNumber;
        this.phoneNumber = phoneNumber;
        this.zipCode = zipCode;
    }
}